    private void ujra(){
        checkKever.setSelected(false);
        gombokTorol(pnlPinKod);
        txtKod.setText("");
        
        pinGombokGenaral();
        gombokAlap();
        gombok=gombTombKomponensekbol();
        alapGombSorrend=gombokListbe();
        gombokraEsemeny();
    }
    
    private void gombokGeneral(JPanel pnl, int meret){
        JButton[] btns = new JButton[meret];
        for (JButton btn : btns) {
            btn = new JButton("");
            pnl.add(btn);
        }
    }
    
    private void pinGombokGenaral(){
        gombokGeneral(pnlPinKod, PIN_GOMBOK_SZAMA);
    }
    
    private void gombokraEsemeny(){
        for (JButton gomb : gombok) {
            gomb.addActionListener(new PinGombListener());
        }
    }
    
    class PinGombListener implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent e) {
            JButton btn = (JButton)e.getSource();
            
            btn.setBackground(Color.CYAN);
            
            String s=txtKod.getText();
            s+=btn.getText();
            
            txtKod.setText(s);
        }
        
    }
    
    private void gombokAlap() {
        JButton[] btns=gombTombKomponensekbol();
        
        for (int i = 0; i <= 8; i++) {
            btns[i].setText(i+1+"");
        }
        
        btns[btns.length-1].setText(0+"");
    }
    
    private JButton[] gombTombKomponensekbol(){
        Component[] comps = pnlPinKod.getComponents();
        JButton[] jbs=new JButton[PIN_GOMBOK_SZAMA];
        
        int ind=0;
        
        for (Component comp : comps) {
            if (comp instanceof JButton) {
                jbs[ind]=(JButton)comp;
                ind++;
            }
        }
        return jbs;
    }

