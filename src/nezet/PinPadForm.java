    class ElrendListener implements ItemListener{

        @Override
        public void itemStateChanged(ItemEvent e) {
            elrendVizszintVagyFuggoleges();
        }
        
    }
    
    private void elrendVizszintVagyFuggoleges(){
        int hely;
        LayoutManager lmPnl;
        if (rdoVizszint.isSelected()) {
            hely=BoxLayout.Y_AXIS;
            lmPnl = new FlowLayout();
            
        }else{
            lmPnl=new BoxLayout(pnlJatek, BoxLayout.Y_AXIS);
            hely=BoxLayout.X_AXIS;
        }
        LayoutManager lm = new BoxLayout(pnlAmobaBeallit, hely);
        pnlAmobaBeallit.setLayout(lm);
        pnlJatek.setLayout(lmPnl);
        
        revalidate();
    }
    
    private void amobaGombokEsemeny(){
        JButton[] btns = new JButton[amobaMeret*amobaMeret];
        Component[] comps=pnlAmoba.getComponents();
        
        int ind=0;
        for (Component comp : comps) {
            if (comp instanceof Component) {
                btns[ind]=(JButton) comp;
                ind++;
            }
        }
        
        for (JButton btn : btns) {
            btn.addActionListener(new AmobaGombListener());
        }
    }
    
    class AmobaGombListener implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent e) {
            JButton btn = (JButton)e.getSource();
            
            String jel= kiJon==0 ? "X":"O";
            btn.setText(jel);
            kiJon=1-kiJon;
        }
        
    }
    
    private void gombokTorol(JPanel pnl){
        pnl.removeAll();
    }
    
    private void kilepes(){
        int valasz=JOptionPane.showConfirmDialog(rootPane, "Kilépsz?", "Kilépés", JOptionPane.YES_NO_OPTION);
        
        if (valasz==JOptionPane.YES_OPTION) {
            System.exit(0);
        }
    }
    
    private ArrayList<JButton> gombokListbe(){
        JButton[] btns=gombTombKomponensekbol();
        ArrayList<JButton> gombok = new ArrayList<JButton>();
        for (int i = 0; i < btns.length; i++) {
            gombok.add(btns[i]);
        }
        return gombok;
    }
    
    private void pinPadFrissit(ArrayList<JButton> al){
        gombokTorol(pnlPinKod);
        for (int i = 0; i < al.size(); i++) {
            pnlPinKod.add(al.get(i));
        }
    }
